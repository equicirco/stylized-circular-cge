"""
    two_country_result_rows(records)

Flatten many two-country experiment records.
"""
two_country_result_rows(records::AbstractVector{<:NamedTuple}) =
    [two_country_result_row(record) for record in records]

"""
    two_country_closed_economy_failures(records; market_tol=1e-5)

Return two-country fiscal rows that did not solve or did not close.
"""
function two_country_closed_economy_failures(records::AbstractVector{<:NamedTuple};
    market_tol::Real = 1.0e-5)
    return RuntimeExperiments.closure_failures(two_country_result_rows(records);
        closure = :two_country_fiscal,
        status = JuMP.MOI.LOCALLY_SOLVED,
        residual_field = :max_abs_market_residual,
        residual_tol = market_tol)
end

"""
    assert_two_country_closed_economy_results(records; market_tol=1e-5)

Validate a two-country fiscal experiment batch and return `records` unchanged.
"""
function assert_two_country_closed_economy_results(records::AbstractVector{<:NamedTuple};
    market_tol::Real = 1.0e-5)
    RuntimeExperiments.assert_closure(two_country_result_rows(records);
        closure = :two_country_fiscal,
        status = JuMP.MOI.LOCALLY_SOLVED,
        residual_field = :max_abs_market_residual,
        residual_tol = market_tol,
        describe = row ->
            "$(row.label): status=$(row.status), max_abs_market_residual=$(row.max_abs_market_residual)")
    return records
end
